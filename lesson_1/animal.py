class Dog:


    def __init__(self, weight, height, color, golos):
        self.weight = weight
        self.height = height
        self.color = color
        self.golos = golos


    def voice(self):
        print(f"{self.golos}")

balto = Dog(50, 120, "White", "gaaaf!")
balto.voice()

